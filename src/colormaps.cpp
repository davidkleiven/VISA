#include "colormaps.hpp"

void visa::Colormaps::setColor( unsigned int indx, sf::Color &color ) const
{
  if ( indx >= 256 ) indx = 255;
  switch( active )
  {
    case ( Colormap_t::VIRIDIS ):
      color.r = 255.0*viridis[indx][0];
      color.b = 255.0*viridis[indx][1];
      color.g = 255.0*viridis[indx][2];
      break;
    case ( Colormap_t::GREYSCALE ):
      color.r = 255.0*indx;
      color.b = 255.0*indx;
      color.g = 255.0*indx;
      break;
    case ( Colormap_t::NIPY_SPECTRAL ):
      color.r = 255.0*nipySpectral[indx][0];
      color.b = 255.0*nipySpectral[indx][1];
      color.g = 255.0*nipySpectral[indx][2];
      break;
    case ( Colormap_t::INFERNO ):
      color.r = 255.0*inferno[indx][0];
      color.b = 255.0*inferno[indx][1];
      color.g = 255.0*inferno[indx][2];
      break;
  }
}

